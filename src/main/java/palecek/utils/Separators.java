package palecek.utils;

public class Separators {
    public static final String TYPE_SEPARATOR = "-"; //separates from head and tail of space
    public static final String DIMENSION_SEPARATOR = ","; //separates dimensions in a position
    public static final String INNER_SEPARATOR = "\\."; //separates inner elements in a head and tail
    public static final String SPACE_SEPARATOR = " "; //separates spaces in a string
    public static final String DIRECTION_SEPARATOR = ":"; //separates directions in a move rule
    public static final String SLASH_SEPARATOR = "/"; //separates orientations in a rule
    public static final String OR_SEPARATOR = "\\|"; //separates or conditions in a rule
    public static final String AND_SEPARATOR = "&"; //separates and conditions in a rule
}
